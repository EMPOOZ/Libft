#include"libft.h"

void	*ft_memset(void *p, int q, size_t a)
{
	char	*f;
	size_t	d;

	f = (char *)p;
	d = 0;
	while (d < a)
	{
		f[d] = q;
		d++;
	}
	return (p);
}
